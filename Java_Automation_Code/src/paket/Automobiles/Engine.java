package paket.Automobiles;

public class Engine {
    private int bhp;
    private String engineBrend;

    public int getBhp() {
        return bhp;
    }

    public void setBhp(int bhp) {
        this.bhp = bhp;
    }

    public String getEngineBrend() {
        return engineBrend;
    }

    public void setEngineBrend(String engineBrend) {
        this.engineBrend = engineBrend;
    }
}
